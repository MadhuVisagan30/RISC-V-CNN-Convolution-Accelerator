`timescale 1ns / 1ps

module instruction_memory(
    input [31:0] pc,
    output [31:0] instruction_code
);

//////////////////////////////////////////////////////////
// MEMORY
//////////////////////////////////////////////////////////

reg [31:0] memory [0:20000];

//////////////////////////////////////////////////////////
// LOAD PROGRAM
//////////////////////////////////////////////////////////

initial
begin
    $readmemh("program_y.mem", memory);
end

//////////////////////////////////////////////////////////
// INSTRUCTION FETCH
//////////////////////////////////////////////////////////

assign instruction_code = memory[pc >> 2];

endmodule