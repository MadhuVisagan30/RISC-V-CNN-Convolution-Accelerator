`timescale 1ns / 1ps

module register_file(
    input clk,
    input rst,

    //////////////////////////////////////////////////////////
    // REGISTER ADDRESSES
    //////////////////////////////////////////////////////////

    input [4:0] read_reg_num1,
    input [4:0] read_reg_num2,
    input [4:0] write_reg_num1,

    //////////////////////////////////////////////////////////
    // WRITE DATA
    //////////////////////////////////////////////////////////

    input [31:0] write_data_dm,

    //////////////////////////////////////////////////////////
    // CONTROL SIGNALS
    //////////////////////////////////////////////////////////

    input reg_write,
    input lb,
    input lui_control,
    input [31:0] lui_imm_val,
    input jump,
    input sw,

    //////////////////////////////////////////////////////////
    // READ DATA
    //////////////////////////////////////////////////////////

    output [31:0] read_data1,
    output [31:0] read_data2,

    //////////////////////////////////////////////////////////
    // DATA MEMORY INTERFACE
    //////////////////////////////////////////////////////////

    output [4:0] read_data_addr_dm,
    output [31:0] data_out_2_dm,

    //////////////////////////////////////////////////////////
    // DEBUG OUTPUTS
    //////////////////////////////////////////////////////////

    output [31:0] x1,
    output [31:0] x2,
    output [31:0] x3,
    output [31:0] x4,
    output [31:0] x5,
    output [31:0] x6,
    output [31:0] x7,
    output [31:0] x8,
    output [31:0] x9,
    output [31:0] x10,
    output [31:0] x11,
    output [31:0] x12,
    output [31:0] x13
    
);

////////////////////////////////////////////////////////////
// REGISTER MEMORY
////////////////////////////////////////////////////////////

reg [31:0] reg_mem [31:0];

integer i;

////////////////////////////////////////////////////////////
// DATA MEMORY ADDRESS
////////////////////////////////////////////////////////////

assign read_data_addr_dm = write_reg_num1;

////////////////////////////////////////////////////////////
// WRITE LOGIC
////////////////////////////////////////////////////////////

always @(posedge clk)
begin

    //////////////////////////////////////////////////////////
    // RESET
    //////////////////////////////////////////////////////////

    if(rst)
    begin

        for(i = 0; i < 32; i = i + 1)
            reg_mem[i] <= 32'b0;

    end

    //////////////////////////////////////////////////////////
    // NORMAL OPERATION
    //////////////////////////////////////////////////////////

    else
    begin

        //////////////////////////////////////////////////////
        // x0 HARDWIRED TO ZERO
        //////////////////////////////////////////////////////

        reg_mem[0] <= 32'b0;


        //////////////////////////////////////////////////////
        // REGISTER WRITE ENABLE
        //////////////////////////////////////////////////////

        if(reg_write)
        begin

            //////////////////////////////////////////////////
            // PREVENT WRITE TO x0
            //////////////////////////////////////////////////

            if(write_reg_num1 != 5'b00000)
            begin

                //////////////////////////////////////////////
                // LUI
                //////////////////////////////////////////////

                if(lui_control)
                    reg_mem[write_reg_num1] <= lui_imm_val;

                //////////////////////////////////////////////
                // LOAD / ALU / MAC / SIMD
                //////////////////////////////////////////////

                else
                    reg_mem[write_reg_num1] <= write_data_dm;

            end
        end
    end
end

////////////////////////////////////////////////////////////
// READ PORTS
////////////////////////////////////////////////////////////

assign read_data1 = reg_mem[read_reg_num1];
assign read_data2 = reg_mem[read_reg_num2];
assign data_out_2_dm = reg_mem[read_reg_num2];

////////////////////////////////////////////////////////////
// DEBUG OUTPUTS
////////////////////////////////////////////////////////////

assign x1 = reg_mem[1];
assign x2 = reg_mem[2];
assign x3 = reg_mem[3];
assign x4 = reg_mem[4];
assign x5 = reg_mem[5];
assign x6 = reg_mem[6];
assign x7 = reg_mem[7];
assign x8 = reg_mem[8];
assign x9 = reg_mem[9];
assign x10 = reg_mem[10];
assign x11= reg_mem[11];
assign x12= reg_mem[12];
assign x13= reg_mem[13];

endmodule