`timescale 1ns / 1ps

module instruction_fetch_unit(

    input clk,
    input reset,

    //////////////////////////////////////////////////////
    // IMMEDIATE ADDRESSES
    //////////////////////////////////////////////////////

    input [31:0] imm_address,
    input [31:0] imm_address_jump,

    //////////////////////////////////////////////////////
    // CONTROL SIGNALS
    //////////////////////////////////////////////////////

    input beq,
    input bneq,
    input bge,
    input blt,
    input jump,

    //////////////////////////////////////////////////////
    // OUTPUTS
    //////////////////////////////////////////////////////

    output reg [31:0] pc,
    output reg [31:0] current_pc

);

//////////////////////////////////////////////////////////
// PROGRAM COUNTER LOGIC
//////////////////////////////////////////////////////////

always @(posedge clk)
begin

    //////////////////////////////////////////////////////
    // RESET
    //////////////////////////////////////////////////////

    if(reset)
    begin
        pc <= 32'b0;
    end

    //////////////////////////////////////////////////////
    // NORMAL PC + 4
    //////////////////////////////////////////////////////

    else if(
        beq  == 0 &&
        bneq == 0 &&
        bge  == 0 &&
        blt  == 0 &&
        jump == 0
    )
    begin
        pc <= pc + 4;
    end

    //////////////////////////////////////////////////////
    // BRANCH
    //////////////////////////////////////////////////////

    else if(beq || bneq || bge || blt)
    begin
        pc <= pc + $signed(imm_address);
    end

    //////////////////////////////////////////////////////
    // JUMP
    //////////////////////////////////////////////////////

    else if(jump)
    begin
        pc <= pc + $signed(imm_address_jump);
    end

end

//////////////////////////////////////////////////////////
// RETURN ADDRESS STORAGE
//////////////////////////////////////////////////////////

always @(posedge clk)
begin

    if(reset)
        current_pc <= 32'b0;

    else
        current_pc <= pc + 4;

end

endmodule