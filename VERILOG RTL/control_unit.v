`timescale 1ns / 1ps

module control_unit(

    input reset,

    input [6:0] funct7,
    input [2:0] funct3,
    input [6:0] opcode,

    //////////////////////////////////////////////////////
    // OUTPUTS
    //////////////////////////////////////////////////////

    output reg [5:0] alu_control,

    output reg lb,
    output reg mem_to_reg,

    output reg bneq_control,
    output reg beq_control,
    output reg bgeq_control,
    output reg blt_control,

    output reg jump,
    output reg sw,

    output reg lui_control,

    output reg reg_write      // ✅ NEW

);

always @(*)
begin

//////////////////////////////////////////////////////////
// DEFAULTS
//////////////////////////////////////////////////////////

alu_control  = 6'b000000;

lb           = 0;
mem_to_reg   = 0;

bneq_control = 0;
beq_control  = 0;
bgeq_control = 0;
blt_control  = 0;

jump         = 0;
sw           = 0;

lui_control  = 0;

reg_write    = 0;     // ✅ IMPORTANT

//////////////////////////////////////////////////////////
// RESET
//////////////////////////////////////////////////////////

if(reset)
begin
    alu_control = 6'b000000;
    reg_write   = 0;
end

//////////////////////////////////////////////////////////
// R-TYPE
//////////////////////////////////////////////////////////

else if(opcode == 7'b0110011)
begin

    reg_write = 1;    // ✅ R-type writes register

    case(funct3)

    //////////////////////////////////////////////////////
    // ADD / SUB / MUL
    //////////////////////////////////////////////////////

    3'b000:
    begin

        if(funct7 == 7'b0000000)
            alu_control = 6'b000001; // ADD

        else if(funct7 == 7'b0100000)
            alu_control = 6'b000010; // SUB

        else if(funct7 == 7'b0000001)
            alu_control = 6'b100001; // MUL

    end

    //////////////////////////////////////////////////////
    // SLL / MULH
    //////////////////////////////////////////////////////

    3'b001:
    begin

        if(funct7 == 7'b0000001)
            alu_control = 6'b100010; // MULH

        else
            alu_control = 6'b000011; // SLL

    end

    //////////////////////////////////////////////////////
    // SLT / MULHSU
    //////////////////////////////////////////////////////

    3'b010:
    begin

        if(funct7 == 7'b0000001)
            alu_control = 6'b100011; // MULHSU

        else
            alu_control = 6'b000100; // SLT

    end

    //////////////////////////////////////////////////////
    // SLTU / MULHU
    //////////////////////////////////////////////////////

    3'b011:
    begin

        if(funct7 == 7'b0000001)
            alu_control = 6'b100100; // MULHU

        else
            alu_control = 6'b000101; // SLTU

    end

    //////////////////////////////////////////////////////
    // XOR
    //////////////////////////////////////////////////////

    3'b100:
        alu_control = 6'b000110;

    //////////////////////////////////////////////////////
    // SRL / SRA
    //////////////////////////////////////////////////////

    3'b101:
    begin

        if(funct7 == 7'b0000000)
            alu_control = 6'b000111; // SRL

        else if(funct7 == 7'b0100000)
            alu_control = 6'b001000; // SRA

    end

    //////////////////////////////////////////////////////
    // OR / MAC
    //////////////////////////////////////////////////////

    3'b110:
    begin

        if(funct7 == 7'b0000010)
            alu_control = 6'b101000; // MAC

        else
            alu_control = 6'b001001; // OR

    end

    //////////////////////////////////////////////////////
    // AND
    //////////////////////////////////////////////////////

    3'b111:
        alu_control = 6'b001010;

    endcase
end

//////////////////////////////////////////////////////////
// I-TYPE
//////////////////////////////////////////////////////////

else if(opcode == 7'b0010011)
begin

    reg_write = 1;    // ✅ I-type writes register

    case(funct3)

    //////////////////////////////////////////////////////
    // ADDI
    //////////////////////////////////////////////////////

    3'b000:
        alu_control = 6'b001011;

    //////////////////////////////////////////////////////
    // SLLI
    //////////////////////////////////////////////////////

    3'b001:
        alu_control = 6'b001100;

    //////////////////////////////////////////////////////
    // SLTI
    //////////////////////////////////////////////////////

    3'b010:
        alu_control = 6'b001101;

    //////////////////////////////////////////////////////
    // XORI
    //////////////////////////////////////////////////////

    3'b100:
        alu_control = 6'b001111;

    //////////////////////////////////////////////////////
    // SRLI / SRAI
    //////////////////////////////////////////////////////

    3'b101:
    begin

        if(funct7 == 7'b0000000)
            alu_control = 6'b010000; // SRLI

        else if(funct7 == 7'b0100000)
            alu_control = 6'b010011; // SRAI

    end

    //////////////////////////////////////////////////////
    // ORI
    //////////////////////////////////////////////////////

    3'b110:
        alu_control = 6'b010001;

    //////////////////////////////////////////////////////
    // ANDI
    //////////////////////////////////////////////////////

    3'b111:
        alu_control = 6'b010010;

    endcase
end

//////////////////////////////////////////////////////////
// LOAD
//////////////////////////////////////////////////////////

else if(opcode == 7'b0000011)
begin

    lb         = 1;

    mem_to_reg = 1;

    reg_write  = 1;   // ✅ LOAD writes register

    alu_control = 6'b001011;

end

//////////////////////////////////////////////////////////
// STORE
//////////////////////////////////////////////////////////

else if(opcode == 7'b0100011)
begin

    sw = 1;

    reg_write = 0;    // ✅ STORE does not write register

    alu_control = 6'b001011;

end

//////////////////////////////////////////////////////////
// BRANCH
//////////////////////////////////////////////////////////

else if(opcode == 7'b1100011)
begin

    reg_write = 0;    // ✅ BRANCH does not write register

    case(funct3)

    //////////////////////////////////////////////////////
    // BEQ
    //////////////////////////////////////////////////////

    3'b000:
    begin
        alu_control = 6'b011011;
        beq_control = 1;
    end

    //////////////////////////////////////////////////////
    // BNE
    //////////////////////////////////////////////////////

    3'b001:
    begin
        alu_control = 6'b011100;
        bneq_control = 1;
    end

    //////////////////////////////////////////////////////
    // BLT
    //////////////////////////////////////////////////////

    3'b100:
    begin
        alu_control = 6'b100000;
        blt_control = 1;
    end

    //////////////////////////////////////////////////////
    // BGE
    //////////////////////////////////////////////////////

    3'b101:
    begin
        alu_control = 6'b011111;
        bgeq_control = 1;
    end

    endcase
end

//////////////////////////////////////////////////////////
// LUI
//////////////////////////////////////////////////////////

else if(opcode == 7'b0110111)
begin

    lui_control = 1;

    reg_write = 1;    // ✅ LUI writes register

end

//////////////////////////////////////////////////////////
// JAL
//////////////////////////////////////////////////////////

else if(opcode == 7'b1101111)
begin

    jump = 1;

    reg_write = 1;    // ✅ JAL writes return address

end


end

endmodule
